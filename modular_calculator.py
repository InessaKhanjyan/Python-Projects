import math

a = 4
b = 5

def power(a,b):
    return math.pow(a,b)
print(power(a,b))

#round the number up
def ceiling(a):
    return math.ceil(a)
print(ceiling(a))

#rounds down
def floor(a):
    return math.floor(a)
print(floor(a))

def factorial(a):
    return math.factorial(a)
print(factorial(a))